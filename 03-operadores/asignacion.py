# operadores de asignacion

edad = 41
print(edad)

edad += 5
# en este caso le suma 5 a la variable edad
edad -= 5
# resta 5 a edad


# incremento

edad = edad + 1
# seria lo mismo que usar += 1

# decremento

edad = edad - 1
# seria lo mismo que usar -= 1

# lo que no existe en ptython es edad++ o bien edad--

# pre-incremento
edad = 1 + edad

# pre-decremento
edad = 1 - edad
